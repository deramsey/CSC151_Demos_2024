class HelloWorld {
    /*
     * This is a block comment.
     * It can have as many lines as we want.
     */
    public static void main(String[] args) {
        System.out.println("Hello \nWorld!"); // \n = new line
        System.out.println("Another\" Hello!"); // \" treats " like a character
        System.out.print("hello \\");// \\ treats \ like a character
        System.out.print("\tworld!");// \t inserts a tab character
    }

}